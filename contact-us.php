<h6>Do you have special menus for guests with food sensitivities?</h6>
<p>We understand how important it is for all of our guests to feel welcomed and cared for here at Veggie Grill. <a href="https://d3vhp0ldxntuok.cloudfront.net/assets/902/src/VG_SpecialMenu_12.14.18.pdf" target="_blank">You can find special menus to fit your dietary needs online here</a>, and you&rsquo;re always welcome to ask our Team Members for help while ordering in our restaurants!<br /><br /><a href="assets/663/src/Spring2018_Chicago_Allergy%20Info.html" target="_blank"></a></p>
<h6>Is your food non-GMO and Organic?</h6>
<p>Many of our staple items and ingredients are non-GMO and/or organic. To list a few: Flour, Sugar, Pasta, Almond Milk, Ketchup, Tempeh, Super Grains, Kale (California locations), Quinoa, and all of our produce. When adding new seasonal menu offerings, we always try to source non-GMO and organic ingredients.</p>
<p>As we wait for regulation of the labeling of GMO ingredients, we are doing our best to provide our guests with the best non-GMO products. Some ingredients that are imported, like spices, have origins that are reliably safe, but are not represented as either GMO or non-GMO. It will be a long time before retail establishments will be able to obtain completely certified non-GMO food items.</p>
<p>We celebrate vegetables when they&rsquo;re in season and try our best to get organic or non-GMO food items when practicable.</p>
<p></p>
<h6>How do you make Chickin&rsquo; without the chicken?</h6>
<p>Our Chickin&rsquo; Veggie protein is produced by GardeinTM. It is made from high-quality organic or non-GMO soybeans, wheat and peas. It also contains ancient grains that are specially seasoned and marinated. This is what makes our Chickin&rsquo; so delicious! We recommend our Buffalo Wings, which are tossed with our spicy buffalo sauce and come with a side of fresh celery sticks and our house-made creamy ranch!</p>
<p></p>
<h6>What is Tempeh?</h6>
<p>Our tempeh is made from organic, non-GMO soybeans and fermented rice culture. It is naturally gluten-friendly and has an earthy, nutty flavor. Try it grilled or blackened on any of our salads, bowls, or as a delicious protein substitution in any sandwich!</p>
<p></p>
<h6>What oil do you cook + fry with?</h6>
<p>We cook and fry with rice bran oil. Rice bran oil begins as organic rice and is a highly nutritious plant oil. It cooks at a very high temperature to create a light, crunchy texture on our veggies and veggie proteins. Rice bran oil is rich in vitamins and antioxidants and we are proud to serve it to you alongside nourishing vegetables and grains!</p>
<p></p>
<h6>Is there a kids&rsquo; menu?</h6>
<p>Yes, there is! We&rsquo;re always excited to feed friends and family of all ages and have a kids&rsquo; menu for little ones. You can find the kids&rsquo; menu on our <a href="menu.php?kids">menu page</a>.</p>
<p></p>
<h6>What happened to my favorite dish?</h6>
<p>We hate to say goodbye to beloved menu items, but we are committed to bringing creativity and new flavors to your plate. Sadly, some items must be retired to bring new items on board. We hope that you will find new favorites on our menu as it continues to grow and celebrate the vegetable!</p>
